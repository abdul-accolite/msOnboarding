package com.accoflow.models;

public class DeveloperType {

	private int frontend;
	private int backend;
	private int fullstack;
	public int getFrontend() {
		return frontend;
	}
	public void setFrontend(int frontend) {
		this.frontend = frontend;
	}
	public int getBackend() {
		return backend;
	}
	public void setBackend(int backend) {
		this.backend = backend;
	}
	public int getFullstack() {
		return fullstack;
	}
	public void setFullstack(int fullstack) {
		this.fullstack = fullstack;
	}

	
}
